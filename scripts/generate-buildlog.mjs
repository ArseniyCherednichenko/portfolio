// Generates src/data/buildlog.ts from the real git history of this repo.
//
// The site claims, on the Colophon, that it "gains one coherent improvement
// most days, so the commit history is part of the story." This turns that
// claim into something you can actually read: the build log page renders the
// genuine commit history — no invention, straight from `git log`.
//
// Run it whenever the log should be refreshed (the daily routine does):
//   node scripts/generate-buildlog.mjs
//
// It is deliberately a committed SNAPSHOT, not a build-time read, so the
// deployed static site never needs a git checkout. The snapshot is stamped
// with GENERATED_AT (the latest commit date) so the page can say "as of ...".

import { execSync } from 'node:child_process'
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const here = dirname(fileURLToPath(import.meta.url))
const out = resolve(here, '../src/data/buildlog.ts')

// How many recent commits to surface on the page. The full count/day stats are
// still computed from the entire history below.
const WINDOW = 60

const US = '\x1f' // field separator
const RS = '\x1e' // record separator

function git(args) {
  return execSync(`git ${args}`, { encoding: 'utf8' })
}

const raw = git(
  `log --format=%h${US}%ad${US}%s --date=short -n ${WINDOW}`,
).trim()

const allDates = git('log --format=%ad --date=short')
  .trim()
  .split('\n')
  .filter(Boolean)

const totalCommits = allDates.length
const firstDate = allDates[allDates.length - 1]
const latestDate = allDates[0]
const activeDays = new Set(allDates).size

// Classify a commit subject into one of three honest buckets by its shape.
// "Add …" introduces something new; "HANDOFF:" keeps the working notes
// current; everything else is a refinement, integration, or fix.
function kindOf(subject) {
  if (/^Add\b/.test(subject)) return 'new'
  if (/^HANDOFF\b/i.test(subject)) return 'docs'
  return 'refine'
}

const commits = raw.split('\n').map((line) => {
  const [hash, date, subject] = line.split(US)
  return { hash, date, subject, kind: kindOf(subject) }
})

const newCount = allDatesKind('new')
function allDatesKind(kind) {
  // Count across the WHOLE history, not just the window, so the headline
  // stat ("N things shipped") stays honest as the window scrolls past them.
  const subs = git('log --format=%s').trim().split('\n')
  return subs.filter((s) => kindOf(s) === kind).length
}

const esc = (s) => s.replace(/\\/g, '\\\\').replace(/'/g, "\\'")

const body = `// AUTO-GENERATED by scripts/generate-buildlog.mjs — do not edit by hand.
// A committed snapshot of this repo's real git history, powering /log.
// Regenerate with: node scripts/generate-buildlog.mjs

export type BuildKind = 'new' | 'refine' | 'docs'

export interface BuildEntry {
  hash: string
  date: string
  subject: string
  kind: BuildKind
}

/** Date (YYYY-MM-DD) of the latest commit in this snapshot. */
export const GENERATED_AT = '${latestDate}'

/** Date of the very first commit — the day the site began. */
export const FIRST_COMMIT = '${firstDate}'

/** Total commits across the whole history. */
export const TOTAL_COMMITS = ${totalCommits}

/** Distinct calendar days with at least one commit. */
export const ACTIVE_DAYS = ${activeDays}

/** How many commits introduced something new ("Add …"), whole history. */
export const NEW_COUNT = ${newCount}

/** The most recent ${WINDOW} commits, newest first. */
export const BUILD_LOG: readonly BuildEntry[] = [
${commits
  .map(
    (c) =>
      `  { hash: '${c.hash}', date: '${c.date}', subject: '${esc(c.subject)}', kind: '${c.kind}' },`,
  )
  .join('\n')}
]
`

writeFileSync(out, body)
console.log(
  `Wrote ${out}: ${commits.length} of ${totalCommits} commits, ${activeDays} active days, ${newCount} new.`,
)
